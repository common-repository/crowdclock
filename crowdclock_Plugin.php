	public function showCrowdClockWidget() {
		$url = 'http://www.crowdclock.com/widget/index.html?businessId=' . $this->getOption("ATextInput") ."&user_key=".$this->getOption("BTextInput");
		$widgetWidth = $this->getOption("widgetWidth") == "" ? '250' : $this->getOption("widgetWidth");
		$widgetHeight = $this->getOption("widgetHeight") == "" ? '350' : $this->getOption("widgetHeight");
		return '<iframe src="'. $url .'" width="'. $widgetWidth .'" height="'. $widgetHeight .'" frameborder="0" seamless></iframe>';
	}


